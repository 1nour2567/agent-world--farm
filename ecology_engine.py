"""
ecology_engine.py — Wildlife ecosystem for Agent Farm
======================================================
Phase W6-1: 13 species across 6 biomes. Food chains, population dynamics,
farm-raiding behavior. Distinguishes hunger-driven vs opportunity attacks.

Design:
  - Species defined in ecology.json (data-driven)
  - Pop/satiation per biome region (not per-tile)
  - Daily tick: eat → satiation change → breed/die → farm interaction
  - Wolf attacks: hunger (satiation < 30) vs opportunity (broken fence)
"""
import json, os, random
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Any


# ═══════════════════════════ DATA CLASSES ═══════════════════════════

@dataclass
class SpeciesPop:
    """Population state for one species in one biome region."""
    species_id: str
    pop: int = 0
    satiation: float = 50.0  # 0-100
    max_pop: int = 10
    last_breed_day: int = 0

@dataclass
class BiomeRegion:
    """Ecology state for one biome patch in the world."""
    biome: str
    populations: Dict[str, SpeciesPop] = field(default_factory=dict)

@dataclass
class EcologyEvent:
    """An event generated by ecology tick — for Agent notification."""
    biome: str
    description: str
    event_type: str  # "crop_damage", "livestock_attack", "observation", "extinction"
    importance: int   # 0-10, feeds into importance scorer
    target_agent: Optional[str] = None
    details: Dict[str, Any] = field(default_factory=dict)


# ═══════════════════════════ ECOLOGY ENGINE ═══════════════════════════

class EcologyEngine:
    """Manages wildlife populations and their interactions with farms."""

    def __init__(self, ecology_path=None):
        if ecology_path is None:
            ecology_path = os.path.join(
                os.path.dirname(os.path.abspath(__file__)), "ecology.json"
            )
        self.species_defs = self._load_species(ecology_path)
        self.regions: Dict[str, BiomeRegion] = {}  # key = biome name
        self._init_regions()
        self.day = 0

    def _load_species(self, path: str) -> dict:
        if not os.path.exists(path):
            return {}
        with open(path, "r", encoding="utf-8") as f:
            return json.load(f).get("species", {})

    def _init_regions(self):
        """Create one region per biome with initial populations."""
        for biome in ["grassland", "forest", "wetland", "hills_mountains", "riverbank", "alluvial_plain"]:
            region = BiomeRegion(biome=biome)
            for sid, sdef in self.species_defs.items():
                if biome in sdef.get("biomes", []):
                    region.populations[sid] = SpeciesPop(
                        species_id=sid,
                        pop=random.randint(1, sdef.get("max_pop", 10) // 2),
                        max_pop=sdef.get("max_pop", 10),
                        satiation=random.uniform(40, 80),
                    )
            self.regions[biome] = region

    def get_region(self, biome: str) -> Optional[BiomeRegion]:
        return self.regions.get(biome)

    # ═══════════════════════ DAILY TICK ═══════════════════════

    def tick_day(self, farms: List[dict]) -> List[EcologyEvent]:
        """Advance ecology by one day. Returns events for agent notification.

        Args:
            farms: List of all farm dicts (to check fences, livestock, crops)
        """
        self.day += 1
        events = []

        for biome, region in self.regions.items():
            biome_events = self._tick_region(region, farms)
            events.extend(biome_events)

        return events

    def _tick_region(self, region: BiomeRegion, farms: List[dict]) -> List[EcologyEvent]:
        events = []

        # 1. FEEDING: predators eat prey, herbivores eat plants
        for sid, pop in list(region.populations.items()):
            sdef = self.species_defs.get(sid, {})
            if pop.pop <= 0:
                continue

            if sdef.get("diet") == "carnivore":
                self._predator_feed(pop, sdef, region)
            else:
                # Herbivores/omnivores: food is always available (grass)
                # but limited by season/carrying capacity
                pop.satiation = min(100, pop.satiation + 15)

        # 2. SATIATION DECAY (all species)
        for sid, pop in region.populations.items():
            if pop.pop <= 0:
                continue
            sdef = self.species_defs.get(sid, {})
            pop.satiation = max(0, pop.satiation - sdef.get("satiation_decay", 0.3))

            # Starvation death
            if pop.satiation < 10:
                deaths = max(1, pop.pop // 4)
                pop.pop = max(0, pop.pop - deaths)
                events.append(EcologyEvent(
                    biome=region.biome, event_type="population_crash",
                    description=f"{sdef.get('name', sid)}种群因饥饿减少了{deaths}只",
                    importance=3, details={"species": sid, "deaths": deaths}
                ))

        # 3. REPRODUCTION
        for sid, pop in region.populations.items():
            if pop.pop <= 0:
                continue
            sdef = self.species_defs.get(sid, {})
            if pop.satiation > sdef.get("breed_threshold", 70) and pop.pop < pop.max_pop:
                if self.day - pop.last_breed_day > 5:
                    if random.random() < sdef.get("breed_chance", 0.2):
                        pop.pop = min(pop.max_pop, pop.pop + 1)
                        pop.last_breed_day = self.day

        # 4. FARM INTERACTION
        for sid, pop in region.populations.items():
            if pop.pop <= 0:
                continue
            sdef = self.species_defs.get(sid, {})
            interaction = sdef.get("farm_interaction", "none")
            if interaction == "none":
                continue

            farm_events = self._farm_interact(sid, pop, sdef, region.biome, farms)
            events.extend(farm_events)

        return events

    def _predator_feed(self, pop: SpeciesPop, sdef: dict, region: BiomeRegion):
        """A predator hunts prey in the same region."""
        prey_ids = sdef.get("preys_on", [])
        available_prey = [
            pid for pid in prey_ids
            if pid in region.populations and region.populations[pid].pop > 0
        ]
        if not available_prey:
            # No prey available — satiation drops faster
            pop.satiation = max(0, pop.satiation - 10)
            return

        # Pick a prey species
        target_pid = random.choice(available_prey)
        prey = region.populations[target_pid]
        prey_def = self.species_defs.get(target_pid, {})

        # Hunt success: depends on predator satiation (hungrier = more desperate)
        hunt_chance = 0.3 if pop.satiation > 30 else 0.6
        if random.random() < hunt_chance:
            # Kill one prey
            prey.pop = max(0, prey.pop - 1)
            pop.satiation = min(100, pop.satiation + 30)
        else:
            pop.satiation = max(0, pop.satiation - 5)  # wasted energy

    def _farm_interact(self, sid: str, pop: SpeciesPop, sdef: dict,
                       biome: str, farms: List[dict]) -> List[EcologyEvent]:
        """Check and execute species-farm interactions."""
        events = []
        interaction = sdef.get("farm_interaction", "none")

        for farm in farms:
            # Rough proximity check — wildlife can reach any farm
            farm_biome = self._farm_biome(farm)
            if farm_biome != biome:
                continue  # only interact with farms in same biome

            if interaction in ("crop_theft", "crop_trample", "tillage_destroy"):
                # Crop damage by herbivores
                crops = farm.get("crops", [])
                if not crops:
                    continue
                damaged = 0
                for crop in crops:
                    if crop.get("gdd_percent", 0) < 95:  # only unharvested
                        dmg = sdef.get("crop_damage_per_night", 0.02) * pop.pop
                        crop["gdd_accumulated"] = max(0, crop.get("gdd_accumulated", 0) - dmg * crop.get("gdd_required", 30))
                        damaged += 1
                if damaged > 0:
                    events.append(EcologyEvent(
                        biome=biome, event_type="crop_damage",
                        description=f"{sdef.get('name', sid)}群破坏了{damaged}株作物",
                        importance=4 if damaged >= 3 else 2,
                        target_agent=farm.get("agent_id", ""),
                        details={"species": sid, "crops_damaged": damaged}
                    ))

            elif interaction == "livestock_attack":
                # Wolf/similar attacks livestock — two modes
                livestock = farm.get("livestock", [])
                if not livestock:
                    continue
                hunger_threshold = sdef.get("hunger_threshold", 30)
                has_fence = "fence" in farm.get("buildings", [])
                fence_damaged = not has_fence or farm.get("fence_damaged", False)

                attack_type = None
                if pop.satiation < hunger_threshold:
                    attack_type = "hunger"
                    kill_chance = sdef.get("livestock_kill_chance_hungry", 0.3)
                elif fence_damaged or not has_fence:
                    attack_type = "opportunity"
                    kill_chance = sdef.get("livestock_kill_chance_opportunity", 0.8)
                else:
                    continue  # well-fed wolf behind intact fence = no attack

                if random.random() < kill_chance * pop.pop:
                    # Pick a target animal
                    target = random.choice(livestock)
                    target_name = target.get("name", target.get("species", "?"))
                    target["health"] = target.get("health", 100) - 40
                    if target["health"] <= 0:
                        livestock.remove(target)
                        desc = f"{sdef['name']}群{'因饥饿' if attack_type == 'hunger' else '从破损围栏钻进来'}攻击了{target_name}——{target_name}死了。"
                    else:
                        desc = f"{sdef['name']}群{'因饥饿' if attack_type == 'hunger' else '从破损围栏钻进来'}攻击了{target_name}——重伤！"

                    sense_desc = sdef.get("sense_attack_hunger" if attack_type == "hunger" else "sense_attack_opportunity", desc)
                    events.append(EcologyEvent(
                        biome=biome, event_type="livestock_attack",
                        description=sense_desc,
                        importance=8 if target.get("health", 0) <= 0 else 6,
                        target_agent=farm.get("agent_id", ""),
                        details={"species": sid, "attack_type": attack_type,
                                 "victim": target_name, "satiation": pop.satiation}
                    ))

                    # Satiation boost for successful hunt
                    pop.satiation = min(100, pop.satiation + 40)

            elif interaction == "egg_theft":
                # Fox steals eggs
                # Simplified: random chance per night
                if random.random() < 0.15 * pop.pop and farm.get("buildings", []):
                    events.append(EcologyEvent(
                        biome=biome, event_type="egg_theft",
                        description="鸡舍里少了几个蛋——狐狸偷吃了",
                        importance=2,
                        target_agent=farm.get("agent_id", ""),
                        details={"species": sid}
                    ))

            elif interaction == "chick_snatch":
                # Eagle snatches chick
                livestock = farm.get("livestock", [])
                chicks = [a for a in livestock if a.get("species") == "chicken" and a.get("age_days", 999) < 7]
                if chicks and random.random() < 0.2 * pop.pop:
                    target = random.choice(chicks)
                    livestock.remove(target)
                    events.append(EcologyEvent(
                        biome=biome, event_type="chick_snatch",
                        description=f"鹰抓走了一只小鸡{target.get('name','?')}",
                        importance=5,
                        target_agent=farm.get("agent_id", ""),
                        details={"species": sid, "victim": target.get("name","?")}
                    ))

            elif interaction == "pasture_competition":
                # Wild sheep compete with domestic sheep for grass
                domestic_sheep = [a for a in farm.get("livestock", []) if a.get("species") == "sheep"]
                if domestic_sheep and pop.pop > 5:
                    # Grazing carrying capacity reduced
                    events.append(EcologyEvent(
                        biome=biome, event_type="pasture_competition",
                        description="野羊群和家羊在抢同一片草场——牧草消耗加速",
                        importance=3,
                        target_agent=farm.get("agent_id", ""),
                        details={"species": sid, "wild_pop": pop.pop}
                    ))

            elif interaction == "beneficial":
                # Frogs eat pests — slight crop protection
                crops = farm.get("crops", [])
                if crops:
                    for crop in crops[:3]:
                        crop["gdd_accumulated"] = crop.get("gdd_accumulated", 0) + 1  # tiny growth boost
                    # No event needed — silent benefit

        return events

    def _farm_biome(self, farm: dict) -> str:
        """Estimate which biome a farm is in from terrain data."""
        terrain = farm.get("terrain", {})
        biome_grid = terrain.get("biome", None)
        if biome_grid and isinstance(biome_grid, list) and len(biome_grid) > 0:
            # Use center tile ~(5,5) as representative
            row = biome_grid[min(5, len(biome_grid)-1)]
            return row[min(5, len(row)-1)] if row else "alluvial_plain"
        return "alluvial_plain"

    # ═══════════════════════ SENSORY OBSERVATIONS ═══════════════════════

    def get_sensory(self, farm: dict, distance_tier: str = "close") -> List[str]:
        """Generate NL sensory observations about wildlife for agent context.
        distance_tier: "distant", "mid", or "close"
        """
        obs = []
        farm_biome = self._farm_biome(farm)
        region = self.regions.get(farm_biome)
        if not region:
            return obs

        field_map = {"distant": "sense_distant", "mid": "sense_mid", "close": "sense_close"}
        field = field_map.get(distance_tier, "sense_close")

        for sid, pop in region.populations.items():
            if pop.pop <= 0:
                continue
            sdef = self.species_defs.get(sid, {})
            sense_text = sdef.get(field, "")
            if sense_text:
                obs.append(sense_text)

        return obs

    def get_wolf_warning(self, farm: dict) -> Optional[str]:
        """Special: detailed wolf threat assessment for agent."""
        farm_biome = self._farm_biome(farm)
        region = self.regions.get(farm_biome)
        if not region or "wolf" not in region.populations:
            return None
        wolf = region.populations["wolf"]
        if wolf.pop <= 0:
            return "这附近已经没有狼了——生态平衡已被打破"
        if wolf.satiation < 20:
            return "⚠ 狼群极度饥饿——对牲畜的威胁极高！加固围栏是第一要务。"
        if wolf.satiation < 40:
            return "狼群最近没怎么捕到猎物——对牲畜有一些威胁。"
        return f"狼群({wolf.pop}只)最近食物充足，不太可能袭击农场。"

    # ═══════════════════════ SERIALIZATION ═══════════════════════

    def to_dict(self) -> dict:
        return {
            "day": self.day,
            "regions": {
                biome: {
                    "populations": {
                        sid: {"pop": p.pop, "satiation": round(p.satiation, 1)}
                        for sid, p in region.populations.items()
                    }
                }
                for biome, region in self.regions.items()
            }
        }

    @classmethod
    def from_dict(cls, data: dict, ecology_path=None) -> "EcologyEngine":
        engine = cls(ecology_path)
        engine.day = data.get("day", 0)
        for biome, rdata in data.get("regions", {}).items():
            if biome in engine.regions:
                region = engine.regions[biome]
                for sid, pdata in rdata.get("populations", {}).items():
                    if sid in region.populations:
                        region.populations[sid].pop = pdata.get("pop", 0)
                        region.populations[sid].satiation = pdata.get("satiation", 50.0)
        return engine


